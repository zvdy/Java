package carArray;

public class carArray {
    public static void main(String[] args) {
    // We create an Array to store all the car brands
    String[] cars = {"Volvo", "BMW", "Ford", "Mazda", "Audi"};
    // for loop to iterate trough the loop and print out the content
    for (int i = 0; i < cars.length; i++) {
        System.out.println("Cars inside array: " + cars[i]);
        }
    }
}